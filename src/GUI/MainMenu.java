    public void writeListEssay(){
        try {
            FileOutputStream file = new FileOutputStream(this.nameEssayFile);
            ObjectOutputStream write_object = new ObjectOutputStream(file);
            int i = 0;
            int size = this.listEssay.size();
            
            while (i < size) {
                Essay essay = this.listEssay.get(i);
                write_object.writeObject(essay);
                i++;
            }
            write_object.close();
            file.close();
        } catch (IOException o) {
            JOptionPane.showMessageDialog(null, "Không tạo và ghi được ra file! Lỗi");
        }
    }
    
