    public void writeListSubject() {
        try {
            FileOutputStream file = new FileOutputStream("listSubject.dat");
            ObjectOutputStream write_object = new ObjectOutputStream(file);
            int i = 0;
            int size = this.listSubject.size();
            while (i < size) {
                Subject subject = this.listSubject.get(i);
                write_object.writeObject(subject);
                i++;
            }
            write_object.close();
            file.close();
        } catch (IOException o) {
            JOptionPane.showMessageDialog(null, "Không tạo và ghi được ra file! Lỗi");
        }
    }

