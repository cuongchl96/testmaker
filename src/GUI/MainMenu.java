    public void readListSubject() {
        try {
            FileInputStream file = new FileInputStream("listSubject.dat");
            ObjectInputStream read_object = new ObjectInputStream(file);
            Subject subject = new Subject();
            while ((subject = (Subject) read_object.readObject()) != null) {
                this.listSubject.add(subject);
            }
            read_object.close();
            file.close();
        } catch (IOException i) {
        } catch (ClassNotFoundException notfound) {
            JOptionPane.showMessageDialog(null, "Không tìm thấy class");
        }
    }

    public void loadData(ArrayList<Subject> listSubject) {
        modelListSubject = new DefaultListModel();
        modelComboBox = new DefaultComboBoxModel();
        modelComboBox.addElement("Chọn một môn học");
        for (int i = 0; i < listSubject.size(); i++) {
            this.modelListSubject.add(i, listSubject.get(i).getCodeSubject());
            this.modelComboBox.addElement(listSubject.get(i).getCodeSubject());
            
        }
        this.jlistSubject.setModel(modelListSubject);
        this.cbSelectSubject.setModel(modelComboBox);
    }

