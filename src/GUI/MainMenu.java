    private void btSaveQuestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveQuestionActionPerformed

        // Neu an vao nut Them cau hoi
        if (this.clickAddQuestion){
            // Them cau hoi tu luan 
            if (this.chooseEssay) {
                Essay essay = new Essay();
                essay.setStatement(this.taNoiDungCauHoi.getText());
                essay.setBarem(this.taBarem.getText());
                essay.setChapter(listChapter);
                essay.setDifficulty(chooseDifficulty);
                essay.setCodeQuestion(this.tfCodeQuestion.getText());
                this.listEssay.add(essay);
                this.modelListQuestion.add(this.listEssay.size()-1,essay.getCodeQuestion());
                this.jlistQuestion.setModel(modelListQuestion);
                this.writeListEssay();
                this.chooseEssay = false;
            }
            
            // Them cau hoi trac nghiem 
            else if (this.chooseMultiChoiceQuestion) {
                MultipleChoiceQuestion multi = new MultipleChoiceQuestion();
                multi.setStatement(this.taNoiDungCauHoi.getText());
                multi.setChapter(listChapter);
                multi.setDifficulty(chooseDifficulty);
                multi.setCodeQuestion(this.tfCodeQuestion.getText());
                multi.setAnswer(listAnswer);                
                this.listMultipleChoiceQuestion.add(multi);
                this.modelListQuestion.addElement(multi.getCodeQuestion());
                this.jlistQuestion.setModel(modelListQuestion);
                this.writeListMultipleChoice();
                this.chooseMultiChoiceQuestion = false;
            }
        }
        // Neu khong an vao nut Them cau hoi, tuc la sua cau hoi
        else {
            if (this.chooseEssay) {
                Essay cur = this.listEssay.get(selectedIndex);
                   
                String codeQuestion = this.tfCodeQuestion.getText();
                String statement = this.taNoiDungCauHoi.getText();
                String barem = this.taBarem.getText();
                String diff = (String)this.cbDifficulty.getSelectedItem();
                
                cur.setBarem(barem);
                cur.setChapter(listChapter);
                cur.setCodeQuestion(codeQuestion);
                cur.setStatement(statement);
                cur.setDifficulty(diff);
           
                this.writeListEssay();
                
                modelListQuestion.remove(this.selectedIndex);
                modelListQuestion.add(this.selectedIndex, this.tfCodeQuestion.getText());
                this.jlistSubject.setModel(modelListSubject); 
                this.chooseEssay = false;
            }
            else if (this.chooseMultiChoiceQuestion) {
                MultipleChoiceQuestion cur = this.listMultipleChoiceQuestion.get(selectedIndex);
                  
                String codeQuestion = this.tfCodeQuestion.getText();
                String statement = this.taNoiDungCauHoi.getText();
                String diff = (String)this.cbDifficulty.getSelectedItem();
                
                cur.setAnswer(listAnswer);
                cur.setChapter(listChapter);
                cur.setCodeQuestion(codeQuestion);
                cur.setStatement(statement);
                cur.setDifficulty(diff);
           
                this.writeListMultipleChoice();
                
                modelListQuestion.remove(this.selectedIndex + this.listEssay.size());
                modelListQuestion.add(this.selectedIndex + this.listEssay.size(),this.tfCodeQuestion.getText());
                this.jlistSubject.setModel(modelListSubject);  
                this.chooseMultiChoiceQuestion = false;
            }
         
        }
    }//GEN-LAST:event_btSaveQuestionActionPerformed

