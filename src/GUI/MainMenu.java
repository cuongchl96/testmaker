    private void cbSelectSubjectItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSelectSubjectItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == ItemEvent.SELECTED){
            this.subjectChoosen = (String) this.cbSelectSubject.getSelectedItem();       
            nameEssayFile = "essay" + this.subjectChoosen + ".dat";
            nameMultipleChoiceQuestionFile = "multiple" + this.subjectChoosen + ".dat";
            
            this.listEssay = new ArrayList<>();
            this.listMultipleChoiceQuestion = new ArrayList<>();
            // Doc file cau hoi tu luan ung voi mon hoc da chon
            try {
                FileInputStream file = new FileInputStream(nameEssayFile);
                ObjectInputStream read_object = new ObjectInputStream(file);
                Essay essay = new Essay();
                while ((essay = (Essay) read_object.readObject()) != null) {
                    this.listEssay.add(essay);
                }
                read_object.close();
                file.close();
            } catch (IOException i) {
            } catch (ClassNotFoundException notfound) {
                JOptionPane.showMessageDialog(null, "Không tìm thấy class");
            }

            // Doc file cau hoi trac nghiem ung voi mon hoc da chon
            try {
                FileInputStream file = new FileInputStream(nameMultipleChoiceQuestionFile);
                ObjectInputStream read_object = new ObjectInputStream(file);
                MultipleChoiceQuestion multi = new MultipleChoiceQuestion();
                while ((multi = (MultipleChoiceQuestion) read_object.readObject()) != null) {
                    this.listMultipleChoiceQuestion.add(multi);
                    
                }
                read_object.close();
                file.close();
            } catch (IOException i) {
            } catch (ClassNotFoundException notfound) {
                JOptionPane.showMessageDialog(null, "Không tìm thấy class");
            }

            // ghi du lieu ra jlistQuestion

            modelListQuestion = new DefaultListModel();
            if (this.listEssay.size() > 0)
                for (Essay listEssay1 : this.listEssay) {
                    modelListQuestion.addElement((String) listEssay1.getCodeQuestion());
                }
            if (this.listMultipleChoiceQuestion.size() > 0)
                for (MultipleChoiceQuestion listMultipleChoiceQuestion1 : this.listMultipleChoiceQuestion) {
                    modelListQuestion.addElement((String) listMultipleChoiceQuestion1.getCodeQuestion());
                }
            this.jlistQuestion.setModel(modelListQuestion);
        }
    }//GEN-LAST:event_cbSelectSubjectItemStateChanged

